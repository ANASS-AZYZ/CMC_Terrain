            'email' => ['sometimes', 'email', 'max:120', 'unique:users,email,'.$user->id],
            'cin' => ['sometimes', 'nullable', 'string', 'max:40'],
            'class_name' => ['sometimes', 'nullable', 'string', 'max:120'],
            'filiere' => ['sometimes', 'nullable', 'string', 'max:120'],
